public class Main {
    public static void main(String[] args) {
        // Sumar los numeros de 1 a 10 usando for

        int sumaTotal = 0;
        for (int i = 1; i <= 10; i++){
            sumaTotal = sumaTotal + i;
        }
        System.out.println("la suma total de los numeros de 1 a 10, usando un for fue: " + sumaTotal);
    }
}